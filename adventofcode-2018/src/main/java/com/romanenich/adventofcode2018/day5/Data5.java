package com.romanenich.adventofcode2018.day5;

public class Data5 {

    private String value;

    public Data5(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    @Override
    public String toString() {
        return "Data5{" +
                "value='" + value + '\'' +
                '}';
    }
}
